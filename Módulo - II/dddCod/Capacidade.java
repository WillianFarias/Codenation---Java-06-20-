public class Capacidade {

  private String potencia;
  private String quantidadePassageiro;

  public String getPotencia() {
    return potencia;
  }

  public void setPotencia(String potencia) {
    this.potencia = potencia;
  }

  public String getQuantidadePassageiro() {
    return quantidadePassageiro;
  }

  public void setQuantidadePassageiro(String quantidadePassageiro) {
    this.quantidadePassageiro = quantidadePassageiro;
  }

}
